<template>
    <div>
      <h1>Lista de Usu치rios</h1>
      <button @click="listarUsuarios">Carregar Usu치rios</button>
      <ul>
        <li v-for="usuario in usuarios" :key="usuario.id">
            Id: {{ usuario.id }} - Usuario: {{ usuario.nome }} - Senha: {{ usuario.senha }}
        </li>
      </ul>
    </div>
  </template>
  
  <script>
  export default {
    name:'FormGetUsuario',
    data() {
      return {
        usuarios: [],
      };
    },
    methods: {
      async listarUsuarios() {
        const apiUrl = 'https://localhost:7096/Usuario/listar-usuario';
        try {
          const response = await fetch(apiUrl, {
            method: 'GET',
            headers: {
              'Accept': 'application/json',
            },
          });
  
          if (!response.ok) {
            throw new Error('Erro ao buscar os Usu치rios');
          }
  
          this.usuarios = await response.json();
        } catch (error) {
          console.error('Erro:', error);
        }
      },
    },
  };
  </script>
  
